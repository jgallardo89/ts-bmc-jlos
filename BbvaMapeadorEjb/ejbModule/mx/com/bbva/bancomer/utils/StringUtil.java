/**
 * The Class StringUtil.
 */
public abstract class StringUtil {
	
	/**
	 * Valida like.
	 *
	 * @param elemento the elemento
	 * @return the string
	 */
	public static String validaLike(String elemento) {
		if(elemento == null || elemento.isEmpty())
			elemento = "%";
		else 
			elemento = "%" + elemento.toUpperCase() + "%";
		return elemento;
	}
}
